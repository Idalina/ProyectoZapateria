
package Modelos;

public class CategoriaCalces {
    
    private int id_categoriacalce;
    private String nombre_categoriacalce;

    public CategoriaCalces() {
    }

    public CategoriaCalces(int id_categoriacalce, String nombre_categoriacalce) {
        this.id_categoriacalce = id_categoriacalce;
        this.nombre_categoriacalce = nombre_categoriacalce;
    }

    public int getId_categoriacalce() {
        return id_categoriacalce;
    }

    public void setId_categoriacalce(int id_categoriacalce) {
        this.id_categoriacalce = id_categoriacalce;
    }

    public String getNombre_categoriacalce() {
        return nombre_categoriacalce;
    }

    public void setNombre_categoriacalce(String nombre_categoriacalce) {
        this.nombre_categoriacalce = nombre_categoriacalce;
    }

   
  
    
    
}
